     url:url,
     type:'post',
     success: function (data) {
     	$('#tipoauto').html(data);
     }
  	});
  }

  function eliminar_a(id){
  	var opcion = confirm('¿Deseas eliminar estos datos?');
  	if(opcion  ==true){
  		var eli="<?php echo base_url();?>CatalogoCtr/eliminar_auto";
  		$.ajax({
         url:eli,
         type:'post',
         data:'id='+id,
         success:function(data){
         	if(data==true){
         		location.href="<?php echo base_url(); ?>CatalogoCtr/CataloGene";
         	}
         }

  		});
  	}


  }

