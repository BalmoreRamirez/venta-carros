			type:"post",
			success:function(data){
			$('#LisGeneral').html(data);
			}

		});
	}

  function llenar_estado(){
  	var url="<?php echo base_url();?>CatalogoCtr/estado";
  	$.ajax({
     url:url,
     type:'post',
     success: function(data) {
     	$('#id_estado').html(data);
     }
  	});
  }

  function select_marca() {
  	
  	var mar="<?php echo base_url();?>CatalogoCtr/marca";
  	$.ajax({
    url:mar,
    type:'post',
